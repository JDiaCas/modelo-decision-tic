/* ─── Public API ──────────────────────────────────────────────────────── */
export function getBloquesParaFase(fase) {
  const mapFn = (bloque) => ({
    ...bloque,
    objetivo: (fase && FASES.includes(fase) && bloque.objetivo[fase]) || bloque.objetivo.base || bloque.objetivo,
    preguntas: bloque.preguntas.map((p) => ({
      id: p.id,
      texto: (fase && FASES.includes(fase) && p.texto[fase]) || p.texto.base || p.texto,
      peso: p.peso,
      answerDirection: p.dir,
      responseOptions: SCALE_MAP[p.scale] || [],
    })),
  })
  return BLOQUES_BASE.map(mapFn)
}

